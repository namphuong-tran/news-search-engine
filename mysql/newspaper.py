class Newspaper:
  def __init__(self, id, title, newspaper, authors, publish_date, keywords, summary, text, url):
    self.id = id
    self.title = title
    self.newspaper = newspaper
    self.authors = authors
    self.publish_date = publish_date
    self.keywords = keywords
    self.summary = summary
    self.text = text
    self.url = url


    
